package model;

public class NodeInfo {
	private int nodeid;
	private String nodename;
	public void setNodeid(int nodeid) {
		this.nodeid = nodeid;
	}
	public int getNodeid() {
		return nodeid;
	}
	public void setNodename(String nodename) {
		this.nodename = nodename;
	}
	public String getNodename() {
		return nodename;
	}
}
